_geo_cache: dict[str, tuple[str, str]] = {}


def get_country(ip: str) -> tuple[str, str]:
    """Возвращает (flag_emoji, country_ru) по IP. Кешируем результаты."""
    if ip in _geo_cache:
        return _geo_cache[ip]

    try:
        resp = requests.get(
            f"http://ip-api.com/json/{ip}?fields=status,countryCode",
            timeout=4,
        )
        data = resp.json()
        if data.get("status") == "success":
            code = data.get("countryCode", "")
            result = COUNTRIES.get(code, ("🌐", "Неизвестно"))
        else:
            result = ("🌐", "Неизвестно")
    except Exception as e:
        logger.debug("geo lookup failed for %s: %s", ip, e)
        result = ("🌐", "Неизвестно")

    _geo_cache[ip] = result
    return result
